function setupFade() {
  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add('show');
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.12 });

  document.querySelectorAll('.fade-in').forEach((element) => observer.observe(element));
}

function toggleMenu(open) {
  const menu = byId('mobileMenu');
  const overlay = byId('menuOverlay');
  if (!menu || !overlay) return;
  menu.classList.toggle('open', open);
  overlay.classList.toggle('open', open);
}

function initHeroSlider() {
  const slides = [...document.querySelectorAll('.hero-slide')];
  const dotsWrap = byId('heroDots');
  if (!slides.length || !dotsWrap) return;

  let index = 0;
  dotsWrap.innerHTML = slides.map((_, slideIndex) => `<button class="hero-dot ${slideIndex === 0 ? 'active' : ''}" type="button" aria-label="Slide ${slideIndex + 1}"></button>`).join('');
  const dots = [...dotsWrap.querySelectorAll('.hero-dot')];

  const show = (nextIndex) => {
    slides[index].classList.remove('active');
    dots[index].classList.remove('active');
    index = nextIndex;
    slides[index].classList.add('active');
    dots[index].classList.add('active');
  };

  dots.forEach((dot, dotIndex) => dot.addEventListener('click', () => show(dotIndex)));
  setInterval(() => show((index + 1) % slides.length), 5200);
}

function initForm() {
  const form = byId('contactForm');
  if (!form) return;
  form.addEventListener('submit', (event) => {
    event.preventDefault();
    alert(currentLang === 'ar' ? 'تم استلام طلبك، وسيتواصل معك فريقنا خلال وقت العمل.' : 'Your request has been received. Our team will contact you during business hours.');
    form.reset();
  });
}

function initLanguage() {
  const button = byId('langBtn');
  if (!button) return;
  button.addEventListener('click', () => {
    currentLang = currentLang === 'ar' ? 'en' : 'ar';
    localStorage.setItem('lang', currentLang);
    render();
  });
}

function initMobileMenu() {
  byId('mobileToggle')?.addEventListener('click', () => toggleMenu(true));
  byId('closeMenu')?.addEventListener('click', () => toggleMenu(false));
  byId('menuOverlay')?.addEventListener('click', () => toggleMenu(false));
  document.querySelectorAll('.mobile-menu a').forEach((link) => {
    link.addEventListener('click', () => toggleMenu(false));
  });
}

